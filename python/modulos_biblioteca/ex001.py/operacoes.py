def soma():
    numb_1 = int(input('Digite o 1° número: '))
    numb_2 = int(input('Digite o 2° número: '))
    r = numb_1 + numb_2
    print(f'A soma de {numb_1} + {numb_2} é {r}')

def subtrair():
    numb_1 = int(input('Digite o 1° número: '))
    numb_2 = int(input('Digite o 2° número: '))
    r = numb_1 + numb_2
    print(f'A subtração de {numb_1} - {numb_2} é {r}')